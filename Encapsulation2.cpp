 #include <iostream>

class Encapsulation
{
private:
     int a,b;
public:
      void show(){
        std::cout << "Enter a value";
        std::cin >> a >> b; 
      }
      void sum(){
        std::cout << "Sum = " << a + b << std::endl;
      }
};

int main()
{
    Encapsulation obj;
    obj.show();
    obj.sum();
    return 0;
}
